function makeStatusHistory(entries: Array<Partial<LeadStatusEntry> & { status: LeadStatusEntry["status"]; offsetDays: number }>): LeadStatusEntry[] {
  return entries.map((e, i) => ({
    id: `lsh-${i}-${Math.random().toString(36).slice(2, 8)}`,
    status: e.status,
    timestamp: daysAgo(e.offsetDays),
    userId: e.userId ?? uid("usr", 1),
    userName: e.userName ?? "System",
    comment: e.comment,
  }));
}

